// ManInTheMiddleScamScreen.js
import React from 'react';
import { StyleSheet, Text, View, TouchableOpacity, StatusBar, ScrollView, Platform } from 'react-native';
import { SafeAreaView, useSafeAreaInsets } from 'react-native-safe-area-context';
import { useNavigation } from '@react-navigation/native';
import { useLanguage } from '../contexts/LanguageContext';

export default function ManInTheMiddleScamScreen() {
  const navigation = useNavigation();
  const insets = useSafeAreaInsets();
  const { translations } = useLanguage();

  const title = translations.scamProtectionTitle_manInMiddle || 'Man-in-the-Middle Scams';
  const description =
    translations.scamManInMiddle_description ||
    'A "Man-in-the-Middle" (MITM) scam is a type of cyberattack where a scammer secretly intercepts and alters communication between two parties who believe they are communicating directly with each other. For example, a scammer might hack into a business’s email and monitor conversations with a client. When the time comes to send an invoice, the scammer sends a fake one with their own bank details. The client, believing the email is from the business, pays the fraudulent invoice. This type of scam can also occur on public Wi-Fi networks where attackers can intercept data being sent and received.';
  const whatToDoTitle = translations.scamManInMiddle_whatToDoTitle || "What to do if you're targeted:";
  const tips = [
    translations.scamManInMiddle_tip1 || 'Independently verify changes to payment details via a trusted number before sending funds.',
    translations.scamManInMiddle_tip2 || 'Avoid sensitive transactions on public Wi‑Fi; use a VPN if necessary.',
    translations.scamManInMiddle_tip3 || 'Ensure https and padlock before entering credentials or payments.',
    translations.scamManInMiddle_tip4 || 'Keep systems updated; use reputable security software.',
  ];

  return (
    <SafeAreaView style={styles.safeArea}>
      <StatusBar barStyle="light-content" backgroundColor="#1A213B" />

      {/* Header */}
      <View style={[styles.headerContainer, { paddingTop: Platform.OS === 'android' ? insets.top : 0 }]}>
        <TouchableOpacity onPress={() => navigation.goBack()} style={styles.backButton}>
          <Text style={styles.backButtonText}>←</Text>
        </TouchableOpacity>
        <Text style={styles.headerTitle}>{title}</Text>
        <View style={{ width: 40 }} /> {/* Spacer to balance header title */}
      </View>

      <ScrollView style={styles.scrollView}>
        <View style={styles.contentContainer}>
          <Text style={styles.descriptionText}>{description}</Text>

          <Text style={styles.whatToDoTitle}>{whatToDoTitle}</Text>
          {tips.map((tip, index) => (
            <View key={index} style={styles.tipItem}>
              <Text style={styles.tipBullet}>•</Text>
              <Text style={styles.tipText}>{tip}</Text>
            </View>
          ))}
        </View>
      </ScrollView>
    </SafeAreaView>
  );
}

